                <h3 class="text-uppercase font-weight-bold">         
                    CONSTRUCCIÓN DE LA HERRAMIENTA YTT
                </h3>
                <p class="font-weight-bold">
                    4.1 Primera versión
                </p>
                <p>
                    Como se mencionó anteriormente, la herramienta YTT fue construida como parte del programa Juntos para la Prevención de la Violencia (JPV) con el fin de focalizar intervenciones en jóvenes en situaciones con alto nivel de riesgo de violencia, además de evaluar el diseño y monitoreo de las intervenciones que se trabajan en este grupo poblacional. Con base en la revisión de la literatura de acuerdo con los principales factores de riesgo y los instrumentos de mayor calidad en el ámbito internacional, se decidió tomar a la SAVRY como referencia para la construcción de una nueva herramienta por parte del programa JPV.
                </p>
                <p>
                    En un inicio se decidió convocar a organizaciones de la sociedad civil e instancias académicas con experiencia en el trabajo con jóvenes en situaciones de riesgo. En un primer momento la función fue recabar y obtener retroalimentación de los métodos con las que focalizan la población juvenil y posteriormente, se hizo un análisis de este conocimiento como indicador para ser considerados en la construcción de la herramienta.
                </p>
                <p>
                    Por tanto, la YTT fue construida como una entrevista semiestructurada que se basaría en los factores de la SAVRY, sin embargo, se incorporaría la retroalimentación llevada a cabo por las organizaciones. Por tanto, la herramienta en su primera versión contuvo un total de 23 reactivos. A partir de ello, inició la primera fase de aplicación.
                </p>
                <p class="font-weight-bold">
                    4.2.-Primera revisión y modificación
                </p>
