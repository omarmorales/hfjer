                <h3 class="text-uppercase font-weight-bold">Introducción</h3>
                <p>
                    Juntos para la Prevención de la Violencia (JPV) es un programa de la Agencia de los Estados Unidos para el Desarrollo Internacional (USAID) en México que persigue el objetivo central de construir capacidades en los distintos niveles de gobierno, organizaciones de la sociedad y sector privado para el diseño, implementación y evaluación de intervenciones de prevención de la violencia y el crimen basadas en evidencia, de forma que éstas puedan ser replicadas y escaladas en el contexto mexicano.
                </p>
                <p>
                    Ante un contexto adverso para la juventud, esta población es la más afectada por el crimen y la violencia, por tanto, surge la necesidad de implementar planes, acciones, programas y modelos de intervención que por un lado, identifiquen el nivel de riesgo en el que se encuentra este tipo de población, valorando por una parte el grado de exposición a la violencia y criminalidad de la comunidad o residencia donde se desenvuelven y, por el otro, mejorar los esfuerzos para disminuir los comportamientos violentos que aumentan la probabilidad a generar actos violentos o delictivos de esas personas.
                </p>
                <p>
                    Desde el punto de vista de la prevención de la violencia que se genera en la juventud, la función de la prevención requiere enfocarse en generar condiciones que permitan disminuir el nivel de riesgo, disminuir los efectos de la exposición a la violencia y el crimen, además de la disminución de los comportamientos de riesgo que pueden generar mayor probabilidad de que a un adolescente o joven cometa un acto violento o un delito.
                </p>
                <p>
                    De esta forma, la herramienta de focalización de jóvenes en riesgo busca crear un proceso en el que las y los actores locales obtengan información para tener mayor claridad del nivel de riesgo de las personas jóvenes con las que se encuentran trabajando en la implementación de modelos de prevención.
                </p>
