            <div class="col-md-12" id="violence-risk-assessment">
                <h3 class="text-uppercase font-weight-bold">VALORACIÓN DEL RIESGO DE VIOLENCIA</h3>
                <p>
                    La tarea de valorar el riesgo de violencia hacia otros ha sido conceptualizada y definida de varias maneras. Por su parte, la prevención de la violencia ha ido incorporando técnicas y procedimientos que tienen como objetivo la estimación de la probabilidad de ocurrencia del comportamiento violento en un futuro. Estas técnicas han sido derivadas de un mejor conocimiento de la naturaleza violenta, de los procesos y factores asociados a ella. El resultado del desarrollo de la valoración de la violencia, ha llevado a considerarla como un proceso con dos implicaciones importantes: la evaluación de riesgo que implica que un individuo actué de forma violenta en el futuro y el desarrollo de planes de acción que permitan reducir el riesgo. El beneficio intencional de la valoración del riesgo desde esta perspectiva es que si se realiza de una manera cuidadosa, esta valoración permitirá evaluar y manejar (gestionar) el riesgo de violencia. Es decir, podrá haber acciones en cuanto a la prevención de la violencia dirigidas a identificar a la personas que se encuentren en riesgo y darles un seguimiento o algún tipo de atención selectiva (Guy 2008; Andrés Pueyo, 2010).
                </p>
                <p>
                    Un cambio importante en cuanto a la valoración de la violencia, ha sido el paso de la evaluación de la peligrosidad a la valoración del riesgo de la violencia. La atribución causal de la conducta violenta al agresor le ha conferido a este una condición de peligrosidad, que se considera inherente a su forma de ser o a su estado psicopatológico. Esta condición se determina mediante una técnica clínico forense que evalúa la capacidad criminal asociada a ciertos rasgos de personalidad o clasifica al sujeto en algún perfil delictivo. Sin embargo, considerar la peligrosidad como la causa de la conducta violenta implica una capacidad de predicción limitada y origina errores de falsos negativos o falsos positivos. Otra limitante de esta categorización es que solamente da lugar a dos tipos de estrategias de contención del riesgo: el internamiento o el tratamiento terapéutico del sujeto (Andrés-Pueyo, 2010).
                </p>
                <p>
                    El gran aporte de la valoración del riesgo es que al ser variable y específico, permite tomar decisiones graduadas y re-evaluables respecto a pronóstico futuro de violencia, no así la identificación de la peligrosidad que lleva a decisiones del tipo todo/nada en el pronóstico. Para valorar el riesgo no necesitamos averiguar las causas de la violencia, sino los factores de riesgo asociados a ella.
                </p>
            </div>
            <div class="col-md-12" id="approaches">
                <h3 class="text-uppercase font-weight-bold">APROXIMACIONES A LA EVALUACIÓN Y PREDICCIÓN DE LA CONDUCTA VIOLENTA</h3>
                <p>
                    La valoración de riesgo se ha basado en conocimientos empíricos y en procedimientos clínicos de obtención de información, también se han utilizado procedimientos actuariales y procedimientos mixtos. Se puede observar que cualquier forma de evaluar el riesgo ha sido estructurada con base en tres decisiones centrales que se realizan en el proceso de evaluación: cuáles factores de riesgo se deben considerar y cómo medirlos, cómo combinar los factores de riesgo y cómo generar la estimación final de riesgo (Guy, 2008). La literatura sobre el tema ha descrito principalmente tres principales formas de evaluación de riesgo. Según Andrews, Bonta y Wormith (2006) actualmente ya se pueden considerar cuatro generaciones.
                </p>
                <p>
                    La primera generación de evaluación de riesgo es el juicio profesional o valoración clínica no estructurada. Es un procedimiento basado en la historia clínica o en una entrevista no estructurada, en el que se toma una decisión con la información obtenida por el evaluador. Andrews, et al (2006) señalan que la evaluación mediante juicios profesionales realizados por clínicos altamente entrenados no es muy precisa.
                </p>
                <p>
                    Las razones de esto se explican en dos vías. El primer problema implica el uso de criterios no observables e informales para la toma de decisiones. En segundo lugar, el clínico realiza algunas preguntas básicas, pero existe una gran flexibilidad en las preguntas realizadas. Aunque se pueden aplicar algunos instrumentos psicométricos y revisar expedientes, lo que se atiende en esos expedientes también está a discreción del profesional. El punto importante en esta toma de decisiones es que son subjetivas, a veces intuitivas y guiadas por presentimientos, es decir, carecen de validez empírica. En estos casos es muy difícil reconstruir el proceso de toma de decisiones.
                </p>
                <p>
                    Las escalas actuariales son consideradas la segunda generación. Este es un procedimiento que emplea instrumentos objetivos ad hoc que permiten cuantificar el riesgo de violencia por medio de un registro detallado de los datos relevantes de la historia personal del sujeto. La información se obtiene mediante una entrevista al sujeto u otras personas relevantes, incluyendo la búsqueda de información en expedientes.
                </p>
                <p>
                    Aunque no exista un consenso en el área de evaluación de riesgo, las evaluaciones actuariales se encuentran por encima del juicio clínico. Los metaanálisis han confirmado el poder de las aproximaciones empíricas y estadísticas sobre las aproximaciones clínicas, por otro lado, se ha encontrado en el enfoque actuarial en el que se suman puntos por cada factor presente ha sido la opción mayormente elegida en la metodología de evaluación de riesgo (Andrews, et al; 2006, Guy, 2008; Andrés-Pueyo 2010).
                </p>
                <p>
                    Aunque los instrumentos de la segunda generación son basados en la evidencia, tienen dos limitaciones principales. Bajo este enfoque no hay ningún modelo teórico que explique el porqué de las conductas violentas y están compuestos casi en su totalidad por ítems históricos estáticos, por lo tanto se considera que su finalidad es la de predecir el riesgo de comportamientos violentos específicos en un periodo temporal concreto, para una población determinada y en un contexto sociocultural específico.
                </p>
                <p>
                    Las escalas de riesgo/necesidad se diferencian de la generación anterior, puesto que miden las necesidades de las personas que han cometido un crimen. Sin embargo, la evidencia de la validez predictiva del componente “necesidad” ha sido problemático. El objetivo es asistir al personal en asignar los recursos de atención y supervisión de manera apropiada (principio de riesgo) y dirigir la intervención (principio de necesidad). Finalmente la cuarta generación hace énfasis en enlazar la evaluación con el manejo de caso. Esto implica adherirse más al principio de riesgo y enfocarse en las necesidades criminógenas. También reconoce el rol de las fortalezas personales en construir una orientación prosocial, la evaluación de los factores de responsividad y maximizar los beneficios del tratamiento, así como monitorear de forma estructurada el caso desde el principio hasta el final.
                </p>
                <p class="font-weight-bold">Juicio Clínico Estructurado</p>
                <p>
                    Una variante que se propone superar las limitaciones de la primera generación de evaluación del riesgo de violencia es el Juicio Clínico Estructurado. Considerando que lo más significativo es analizar los factores de riesgo asociados empíricamente a la violencia, el juicio clínico estructurado es un protocolo a modo de guía de valoración de riesgo. Esto facilita la toma de decisiones del profesional al disponer éste de una fuente de información relevante y necesaria.
                </p>
                <p>
                    La valoración del riesgo por medio del juicio clínico estructurado supone algo más que predecir la violencia futura. Al valorar la presencia e intensidad de los factores de riesgo estáticos y dinámicos se pueden tomar diversos tipos de decisiones:
                </p>
                <ul>
                    <li>
                        Proponer procedimientos de gestión del riesgo individualizados y apropiados al momento de la evaluación.
                    </li>
                    <li>
                        Iniciar la prevención de la violencia futura.
                    </li>
                    <li>
                        Diseñar programas de tratamiento concretos en función de las características de los actores de violencia.
                    </li>
                </ul>
                <p>
                    En cualquier caso, las guías de valoración de riesgo son herramientas útiles al servicio de los profesionales y no pueden en modo alguno sustituir a éstos en la toma de decisiones.
                </p>
                <p>
                    Los resultados de un metaanálisis examinando la validez predictiva del modelo de juicio clínico estructurado apoya la utilidad del mismo, especialmente cuando se utiliza la sumatoria de rangos de riesgo, así mismo señala que no existe superioridad entre la utilización de este modelo con el modelo actuarial. Sin embargo el modelo de juicio clínico estructurado permite conocer factores adicionales que son relevantes cuando se trabaja desde una aproximación práctica en la prevención del riesgo (Guy 2008).
                </p>
            </div>
            <div class="col-md-12" id="tools">
                <h3 class="text-uppercase font-weight-bold">         
                    PRINCIPALES HERRAMIENTAS PARA MEDIR EL RIESGO DE LA VIOLENCIA
                </h3>
                <p>
                    Es importante recalcar que la medición de la violencia y su riesgo ha tenido una relevancia en el campo de las áreas sociales, por ello se ha llevado a cabo una serie de esfuerzos para generar y fortalecer las herramientas de evaluación que permitan un diagnóstico más certero (Singh, Condemarín y Folino, 2013).
                </p>
                <p>
                    Para la realización del prototipo de la herramienta YTT, el programa Juntos para la Prevención de la Violencia (JPV) seleccionó algunas herramientas conocidas a nivel internacional que identifican y miden diversos comportamientos y factores de riesgo. Las herramientas más significativas que se analizaron fueron:
                </p>
                <ul>
                    <li>
                        <span class="font-weight-bold">Child and Youth Resilient Measure (CYRM-28).</span> Permite detectar y explorar los recursos (individuales, relacionales, comunitarios y culturales) que poseen jóvenes de 12 a 23 años para reforzar su capacidad de resiliencia (Ungar y Liebenberg, 2005).
                    </li>
                    <li>
                        <span class="font-weight-bold">Development Assets Profile (DAP).</span> Evalúa las fortalezas internas y apoyos externos que influyen en el éxito de un joven entre 12 y 18 años en la escuela y en la vida (Scales, 2011).
                    </li>
                    <li>
                        <span class="font-weight-bold">Youth Level Service/Case Management Inventory (YLS/CMI).</span> Se utiliza para evaluar jóvenes de entre 12 y 18 años y su incidencia sobre delitos (Hoge & Andrews, 2006).
                    </li>
                    <li>
                        <span class="font-weight-bold">Prueba juvenil (YSET).</span> Es útil para identificar la presencia de factores de riesgo que, según estudios, están relacionados con la participación adolescente temprana en pandillas (Terence, Hayeslip, Lyons and Denver, 2010).
                    </li>
                    <li>
                        <span class="font-weight-bold">Structured Assessment of Violence Risk in Youth (SAVRY).</span> Su campo de acción es evaluar y valorar el riesgo de reincidencia en adolescentes que han delinquido. Se enfoca en jóvenes de entre 14 y 18 años (Borum, 2002).
                    </li>
                </ul>
                <p>
                    Con esta revisión y bajo el enfoque del JPV, cuyo objetivo se enfoca en detectar los factores de riesgo que se encuentran en población focalizada, además de facilitar a los entrevistadores un guía para dar nombre a sus observaciones previas con base a su marco de referencia y su experiencia, se eligió la SAVRY como referencia para la construcción de una nueva herramienta que se ajuste a las necesidades de la población en cuestión.
                </p>
                <p class="font-weight-bold">
                    Herramienta de Valoración del Riesgo de Violencia Juvenil (SAVRY).
                </p>
                <p>
                    La SAVRY es un instrumento que sirve para estimar el riesgo de reincidencia de violencia en adolescentes. Fue elaborado como guía en las valoraciones y toma de decisiones para profesionales sobre la situación personal y socio familiar de los jóvenes.
                </p>
                <p>
                    Se compone de 24 ítems de riesgo, entre los 10 son ítems históricos, 6 ítems sociales/contextuales, 8 individuales y 6 de protección. Estos factores que explora el SAVRY, de acuerdo con los autores y a la investigación empírica (Borum, Bartel y Forth, 2003), son los factores que pueden incidir en la ocurrencia de nuevas conductas delictivas.
                </p>
                <p>
                    Los diferentes ítems de riesgo que componen el SAVRY se valoran como de riesgo bajo, moderado o alto. Los factores de protección se valoran como presente o ausente. Es fundamental la argumentación de la valoración y puntuación de cada factor, para una correcta gestión del riesgo. Por el modo de codificación de sus factores, no se les asigna un valor numérico, sino una valoración profesional. Los ítems son:
                </p>
                <p class="font-weight-bold">
                    Ítems históricos:
                </p>
                <ul>
                    <li>Ítem 1. Violencia previa.</li>
                    <li>Ítem 2. Historia de actos delictivos no violentos.</li>
                    <li>
                        Ítem 3. Inicio temprano de la violencia.
                    </li>
                    <li>
                        Ítem 4. Seguimiento en el pasado.
                    </li>
                    <li>
                        Ítem 5. Intentos de autolesión o suicidio anteriores.
                    </li>
                    <li>
                        Ítem 6. Exposición a la violencia en el hogar
                    </li>
                    <li>
                        Ítem 7. Historia de maltrato infantil.
                    </li>
                    <li>
                        Ítem 8. Delincuencia de los padres o cuidadores.
                    </li>
                    <li>
                        Ítem 9. Separación temprana de los padres o cuidadores.
                    </li>
                    <li>
                        Ítem 10. Bajo rendimiento en la escuela.
                    </li>
                </ul>
                <p class="font-weight-bold">
                    Ítems culturales:
                </p>
                <ul>
                    <li>Ítem 11. Delincuencia en el grupo de iguales.</li>
                    <li>Ítem 12. Rechazo del grupo de iguales.</li>
                    <li>
                        Ítem 13. Estrés experimentado e incapacidad para enfrentar dificultades.
                    </li>
                    <li>
                        Ítem 14. Escasa habilidad de los padres para educar.
                    </li>
                    <li>
                        Ítem 15. Falta de apoyo personal / social de otros adultos.
                    </li>
                    <li>
                        Ítem 16. Entorno marginal.
                    </li>
                </ul>
                <p class="font-weight-bold">
                    Ítems individuales:
                </p>
                <ul>
                    <li>Ítem 17. Actitudes negativas.</li>
                    <li>Ítem 18. Asunción de riesgos / impulsividad.</li>
                    <li>
                        Ítem 19. Problemas de consumo de sustancias.
                    </li>
                    <li>
                        Ítem 20. Problemas con el manejo del enfado.
                    </li>
                    <li>
                        Ítem 21. Bajo nivel de empatía / remordimiento.
                    </li>
                    <li>
                        Ítem 22. Problemas de concentración / hiperactividad.
                    </li>
                    <li>
                        Ítem 23. Baja colaboración en las intervenciones.
                    </li>
                    <li>
                        Ítem 24. Bajo interés / Compromiso escolar o laboral.
                    </li>
                </ul>
                <p class="font-weight-bold">
                    Ítems de protección:
                </p>
                <ul>
                    <li>Ítem 1. Implicación prosocial.</li>
                    <li>Ítem 2. Apoyo social fuerte.</li>
                    <li>
                        Ítem 3. Fuertes vínculos y lazos con al menos un adulto prosocial.
                    </li>
                    <li>
                        Ítem 4. Actitud positiva hacia las intervenciones y la autoridad.
                    </li>
                    <li>
                        Ítem 5. Fuerte compromiso con la escuela o el trabajo.
                    </li>
                    <li>
                        Ítem 6. Perseverancia con rasgo de personalidad.
                    </li>
                </ul>
                <p class="font-weight-bold">
                    Evidencia de la aplicación de SAVRY
                </p>
                <p>
                    Se ha iniciado una revisión bibliográfica sobre el estado de evidencia con respecto al SAVRY, por ejemplo, Lodewijks y colaboradores (2008) elaboraron un estudio prospectivo para examinar la validez predictiva de la versión holandesa del SAVRY mediante la asociación de las relaciones entre las puntuaciones de esta evaluación y los puntajes de varios tipos de comportamiento perturbador. Se aplicó a 66 adolescentes varones detectados por graves problemas de comportamiento o delitos graves. Identificaron que la confiabilidad de los puntajes de SAVRY fue buena. La validez predictiva de SAVRY para la violencia física contra las personas fue excelente (AUC = .80, r = .33; AUC = .86, r = .48). El SAVRY también tenía una buena validez predictiva respecto a la violencia que los jóvenes pueden ejercer hacia objetos. Además, concluyen que cuenta con una validez predictiva aceptable sobre amenazas verbales y violaciones de las reglas, pero no en abuso verbal.
                </p>
                <p>
                    Un estudio identificó que el SAVRY cuenta mayor validez predictiva en comparación con el Nivel Juvenil de Servicio / Inventario de Manejo de Casos (YLS / CMI) y la Lista de Verificación de Psicopatía: Versión Juvenil (PCL: YV) (Penney, Lee y Moretti, 2010).
                </p>
                <p>
                    Singh et al. (2011), analizaron 68 estudios basados en 25,980 participantes en 88 muestras independientes concluyendo que el SAVRY produjo las tasas más altas de validez predictiva a comparación del resto de instrumentos o herramientas utilizadas. Realizaron un metaanálisis para evaluar el grado de validez predictiva de las herramientas comúnmente utilizadas para evaluar el riesgo de violencia, comportamiento sexual y criminal, incluyeron 73 estudios compuestos de muestras 24 847 participantes de 13 países, concluyen que las herramientas de evaluación de riesgos son ampliamente utilizadas en la clínica y la configuración de la justicia penal, su precisión predictiva varía según sobre cómo se usan. Identifican individuos de bajo riesgo con altos niveles de precisión, pero su uso como únicos determinantes de la detención.
                </p>
                <p>
                    Gammelgård, Koivisto, Eronen, y Kaltiala (2015), utilizaron el SAVRY en entornos de psiquiatría de adolescentes analizando 200 casos concluyendo que es método válido y útil para evaluar los riesgos a corto y largo plazo de delitos violentos y no violentos cometidos por jóvenes en entornos psiquiátricos y de justicia penal.
                </p>
                <p>
                    Ortega, García y Zaldívar (2017) examinaron la validez predictiva del SAVRY en un grupo de jóvenes delincuentes de España. La muestra la muestra se compuso por 594 menores con una edad de entre14 y 18 años en el momento en que cometieron el acto delictivo. Identificaron que el SAVRY pudo diferenciar entre delincuentes más jóvenes de bajo y alto riesgo. Las puntuaciones medias en el factor de riesgo son mayores en el grupo de delincuentes reincidentes, el grupo de no reincidentes muestra puntuaciones medias más altas en el dominio de protección. La precisión del instrumento es alta, concluyen que los resultados obtenidos respaldan el buen funcionamiento de SAVRY con muestras no inglesas. Estos mismos resultados se observan en otros estudios. En China (Jiansong, Katrina, Xia, Chen y Xiaoping, 2017) coinciden en que el SAVRY puede utilizarse para informar el desarrollo y la evaluación de enfoques efectivos de evaluación y gestión del riesgo de violencia para delincuentes juveniles varones detenidos en un centro de detención juvenil.
                </p>
